#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

// j is the iterating index within the 2nd argvs, 3rd argv depending on ith argv
// i = 1 skip the 1st variable which is the program name
int	main(int argc, char **argv)
{
	int	i;
	int	j;

	i = 1;
	if (argc > 1)
	{
		while (i < argc)
		{
			j = 0;
			while (argv[i][j] != '\0')
			{
				ft_putchar(argv[i][j]);
				j++;
			}
			i++;
			ft_putchar('\n');
		}
	}
	return (0);
}
